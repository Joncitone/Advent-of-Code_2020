function dictionaryPopulator(bagAndContents) {
  for (bag of bagAndContents) {
    let bagColor = bag[0];

    let contents = {};

    let bagsInside = bag[1].split(',');

    for (currentBag of bagsInside) {
      let curentBagColor = currentBag.slice(2);
      let quantity = Number(currentBag[0]);

      if (quantity === 0) continue;

      contents[curentBagColor] = quantity;
    }

    if (Object.keys(contents).length === 0) {
      bagDictionary[bagColor] = null;
      continue;
    }

    bagDictionary[bagColor] = contents;
  }
}

