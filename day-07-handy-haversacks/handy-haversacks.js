// Now the strategy will be to traverse all bags in the dictionary
// With the use of a recursive helper function to find bags containing shiny gold

function atLeastOneShinyGoldCounter() {
  let shinyGoldCount = 0;

  //traverse all the bags in the bagDictionary
  for (const bagColor in bagDictionary) {
    dfs(bagColor);
  }

  function dfs(bagColor, visited = new Set(), goldCount = 0) {
    visited.add(bagColor);

    if (bagColor === '') return 0;
    const bagContents = bagDictionary[bagColor];

    for (const innerBag in bagContents) {
      if (innerBag === 'shiny gold') {
        return 1;
      }

      if (!visited.has(innerBag)) {
        return goldCount + dfs(innerBag, visited, goldCount);
      }
    }

    return goldCount;
  }

  return shinyGoldCount;
}

