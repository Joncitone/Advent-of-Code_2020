let rights = [1, 3, 5, 7, 1];
let downs = [1, 1, 1, 1, 2];

let numsToMultiply = [];

function treeCounter(inputArray, rightStep, downStep) {
  let treeCount = 0;
  let currentRow = 0;
  let currentTree = 0;
  let finalRow = inputArray.length;
  let rowLength = inputArray[0].length;

  while (currentRow < finalRow) {
    if (currentTree >= rowLength) {
      currentTree = currentTree % rowLength;
    }

    if (inputArray[currentRow][currentTree] === '#') {
      treeCount++;
    }

    currentTree += rightStep;
    currentRow += downStep;
  }

  return treeCount;
}

for (let i = 0; i < rights.length; i++) {
  numsToMultiply.push(treeCounter(inputArray, rights[i], downs[i]));
}

let numsMultiplied = numsToMultiply.reduce((acc, curr) => {
  return (acc *= curr);
}, 1);

console.log(numsMultiplied);
