let binarySeatArray = input.split('\n');

let seatIDs = [];

function seatIDFinder(binarySeatArray) {
  for (seat of binarySeatArray) {
    let rowString = seat.slice(0, 7);
    let colString = seat.slice(7);

    let row = seatLocator(rowString);
    let col = seatLocator(colString);

    let seatID = row * 8 + col;
    seatIDs.push(seatID);
  }

  return Math.max(...seatIDs);
}

function seatLocator(string) {
  let lower = 0;
  let upper = 2 ** string.length - 1;

  let middle = Math.floor((upper + lower) / 2);

  let currIndex = 0;

  while (upper - lower > 1) {
    if (string[currIndex] === 'F' || string[currIndex] === 'L') {
      upper = middle;
      middle = Math.floor((upper + lower) / 2);
      currIndex++;
    }
    if (string[currIndex] === 'B' || string[currIndex] === 'R') {
      lower = middle;
      middle = Math.floor((upper + lower) / 2);
      currIndex++;
    }
  }

  if (string[currIndex] === 'F' || string[currIndex] === 'L') return lower;
  else return upper;
}

