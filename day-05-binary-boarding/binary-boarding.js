function seatFinder(seatIDs) {
  seatIDs.sort((a, b) => a - b);

  for (let i = 0; i < seatIDs.length - 1; i++) {
    let currSeat = seatIDs[i];
    let nextSeat = seatIDs[i + 1];
    if (nextSeat !== currSeat + 1) {
      return currSeat + 1;
    }
  }
}

